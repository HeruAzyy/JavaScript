// if Statement
let nilai = 75

if (nilai >= 75) {
  console.log('Selamat anda lulus')
} else {
  console.log('Maaf anda belum lulus')
}

// ternary Statement
// ? saat kondisi terpenuhi (sebagai if) - true
// : saat kondisi tidak terpenuhi (sebagai else) - false
let score = 80

score >= 75 ? console.log('Lulus') : console.log('Tidak Lulus')
